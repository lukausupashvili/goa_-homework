current_age = int(input("მომხმარებლის მიმდინარე ასაკი: "))
current_year = int(input("მომხმარებლის მიმდინარე წელი: "))
years_to_travel = int(input("რამდენი წლით სურს დროში მოგზაურობა? "))


future_year = current_year + years_to_travel
future_age = current_age + years_to_travel


print(f"თქვენი მომავალი წელი იქნება: {future_year}")
print(f"თქვენი ასაკი იმ დროს იქნება: {future_age} წლის")